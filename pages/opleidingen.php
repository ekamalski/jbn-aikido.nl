In september 2014 start een niveau 3 opleiding.
<br/><br/> 
De opleiding omvat 17 lesdagen van 6 uur van ca. 10.00 uur tot ca. 17.00 uur, 
plus 2 dagen voor de afname van de  Proeven Van Bekwaamheid  Lesgeven (PVB).   
<br/><br/> 
De cursusdagen zijn gepland op zaterdagen van september t/m april, 
de PVB  Lesgeven worden afgenomen in juni. De eerste lesdag is zaterdag 6 september 2014. 
<br/><br/>  
De locatie wordt nog bekend gemaakt. 
