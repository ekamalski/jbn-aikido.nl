<h2> Gebruik van de kalender </h2>

De kalender biedt veel extra mogelijkheden. Hier een korte opsomming van die mogelijkheden:
<ul>
<li> De grijze knop "vandaag" gaat direct terug naar de kalender veld vandaag. </li>
<li> De blauwe knoppen direct naast de knop "vandaag" gaan naar de volgende/vorige maand. </li>
<li> De tabs "Week", "Maand", "Agenda" geven de inhoud in respectievelijk week, maand en agenda view weer. </li>
<li> Het icoontje met de printer met tekst "Afdrukken" laat je agenda inhoud afdrukken (of opslaan als PDF-document).
<br/> De print (PDF-document) is afhankelijk van de tab "Week", "Maand", "Agenda" die je gekozen hebt. 
Ik zelf vind een print van de agenda het meest bruikbaar als print-out. </li>
<li> Als je op een agenda-item klikt, ga bijvoorbeeld naar 10 september en klik op 
"Heereveen, Claude", dan verschijnt meer gedetailleerde informatie.
De gedetailleerde informatie laat de volledige titel zien 
(bijvoorbeeld: Heereveen, Claude Pellerin). 
Merk op dat "Waar" de mogelijkheid bied om een kaart (google maps) van de locatie te tonen</li>
<li> Het kruisje "X" rechts boven sluit de gedetailleerde informatie </li>
</ul>
