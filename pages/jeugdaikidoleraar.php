<p><strong>In januari 2015 start een masterclass Jeugdaikidoleraar van 4 cursusdagen en een toetsingsdag.</strong></p>
<p>De opleiding Jeugdaikidoleraar (JAL) is een aanvulling op de opleiding Aikidoleraar niveau 3 (AL3). Je leert lessen aan jeugd op kyugraadniveau te verzorgen en te evalueren. De opleiding geeft géén extra bevoegdheden. Wél extra kennis en vaardigheden op het gebied van: didactiek, pedagogiek, begeleiding van jeugdgroepen, ontwikkelingspsychologie (inclusief stoornissen), contacten met ouders, werk- en spelvormen en fysiologie.</p>

<h2>Toelatingseisen</h2>
<p>Je kunt aan de opleiding JAL in 2015 deelnemen als je:</p>
<ul>
	<li>JBN-lid bent;</li>
	<li>op 31 december 2014 18 jaar of ouder bent;</li>
	<li>bij aanvang van de opleiding minimaal de 1e dan aikido hebt;</li>
	<li>een licentie aikidoleraar 3 (AL3) hebt; óf de opleiding JAL parallel met de opleiding AL3 volgt als aanvullende opleiding voor een extra aantekening</li>
</ul>

<h2>Duur</h2>
<p>De opleiding omvat 4 opleidingsdagen (van 10.00 uur tot 16.00 uur) en een toetsingsdag. Verzuim van opleidingsdagen is in principe niet toegestaan. De opleidingsdagen zijn:
<ul>
	<li>31 januari 2015</li>
	<li>28 februari</li>
	<li>28 maart</li>
	<li>16 mei</li>
</ul>
<p>Tussen de opleidingsdagen heb je volop gelegenheid om de lesstof in de praktijk toe te passen. De datum van de toetsingsdag wordt later bekendgemaakt.</p>

<h2>Kosten</h2>
<p>De kosten voor deelname bedragen € 180 voor JBN-leden (€ 230 voor niet-leden). Alle kosten van de proeve van bekwaamheid (PVB), deelname en opleidingsmateriaal zijn hierbij inbegrepen.</p>

<h2>Inhoud</h2>
<p>Je wordt stapsgewijs voorbereid op een toetsing, waarin het functioneren als jeugdaikidoleraar centraal staat. Tijdens de opleiding voer je drie praktijkopdrachten uit:</p>
<ul>
	<li>schrijf een essay over jouw visie op jeugdaikido;</li>
	<li>geef 2 lessen aan jeugdgroepen buiten jouw eigen dojo (gedocumenteerd met lesplan, feedback en zelfreflectie).</li>
</ul>
<p>Je kiest een ervaren jeugdaikidoleraar die jou daarbij helpt als praktijkbegeleider. In het essay behandel je in ieder geval deze onderwerpen:</p>
<ul>
	<li>aan welke leeftijdsgroep wil je lesgeven</li>
	<li>hoe wil je omgaan met examens</li>
	<li>hoe wil je omgaan met ouders</li>
	<li>welke valkuilen voorzie je voor jezelf als je gaat lesgeven</li>
	<li>welke competenties heb je al waardoor jij geschikt bent als jeugdtrainer</li>
</ul>
<p>De opleidingen AL3 en JAL zijn goed op elkaar afgestemd. In de opleiding AL3 worden didactiek en fysiologie algemeen behandeld, zodat je in de opleiding JAL dieper kunt ingaan op jeugddidactiek en jeugdfysiologie. Heb je eerder de licentie AL3 of AL4 behaald, dan heb de benodigde algemene kennis en ervaring natuurlijk al op zak.</p>

<h2>Onderwerpen</h2>
<p>Tijdens de vier opleidingsdagen behandel je deze onderwerpen in theorie én praktijk:</p>
<ol>
	<li>ontwikkelingspsychologie en fysiologie
		<ul>
			<li>theorie</li>
			<li>toepassen in de praktijk inclusief blessurebehandeling en -preventie</li>
		</ul>
	</li>
	<li>pedagogiek
		<ul>
			<li>theorie, motiveren, straffen en belonen</li>
			<li>omgaan met ontwikkelingsstoornissen in de praktijk</li>
		</ul>
	</li>
	<li>didactiek
		<ul>
			<li>theorie van didactische werkvormen (specifiek gericht op de jeugd)</li>
			<li>spel/werkvormen in de praktijk</li>
		</ul>
	</li>
	<li>organisatie en communicatie
		<ul>
			<li>theoretische behandeling examens, ouders, werving</li>
			<li>parktijkles wapens, etiquette, gebruik terminologie</li>
		</ul>
	</li>
</ol>

