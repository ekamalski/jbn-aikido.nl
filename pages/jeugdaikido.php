<h2>Inhoud</h2>
<p> 
Je wordt stapsgewijs voorbereid op een toetsing, waarin het functioneren als 
leraar jeugdaikido centraal staat. Tijdens de opleiding voer je twee opdrachten uit:
<ol>
<li> je schrijft een essay over jouw visie op jeugdaikido
<li> je geeft twee lessen aan jeugdgroepen buiten jouw eigen dojo 
    (gedocumenteerd met lesplan, feedback,  zelfreflectie en video-opnames van 
    beide lessen op CD/DVD).
</ol>


<h2>Onderwerpen</h2>
Tijdens de vier opleidingsdagen komen de  onderstaande onderwerpen aan de orde 
in de theorie én de praktijk:
<ul>
<li>ontwikkelingspsychologie en fysiologie 
<li>blessurebehandeling en -preventie
<li>pedagogiek, motiveren, straffen en belonen
<li>omgaan met stoornissen en gedragsproblemen van kinderen
<li>didactiek, spel/werkvormen in de praktijk, gebruik van  wapens, etiquette, gebruik terminologie, examens
<li>contact met ouders
</ul>

