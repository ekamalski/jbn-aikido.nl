Ki-aikido is een bewegingsvorm voortgekomen uit de traditionele Japanse 
krijgskunsten met als uitgangspunt dat de geest het lichaam beweegt. 
Als de kunst van het leiden van de geest wordt beheerst kan men op een 
eenvoudige manier een sterke tegenstander weerstaan. 
Ki-aikido is geen zelfverdediging op zich, maar in de technieken en bewegingen 
zijn elementen terug te vinden uit de filosofie, psychologie en de dynamica. 
Door op deze manier ki-aikido te beoefenen worden lichaam en geest getraind, 
de gezondheid verbeterd en het zelfvertrouwen verhoogd. 
Ki-aikido is zeker geen vechtsport en kent ook geen wedstrijden. 
