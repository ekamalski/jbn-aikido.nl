Aikido is een sierlijke en effectieve budovorm voor jong en oud. 
Deze Japanse zelfverdedigingskunst kan zowel door dames als heren op elke 
leeftijd beoefend worden. 
Aikido is een ideale manier van lichamelijke ontspanning en geestelijke 
concentratie. 
Aikido is een oosters principe waarbij 'meegeven om te overwinnen' centraal 
staat. 
Een aanval keert zich in Aikido altijd tegen de aanvaller. 
Door gebruik te maken van aanvalskracht en richting weet de Aikidoka door middel 
van werp- of klemtechnieken de aanvaller onder controle te brengen. 
De bewegingsvorm in Aikido is elegant en soepel waarbij kracht een bescheiden 
rol speelt. 
Aikidoka's dragen over hun budokleding een wijde zwarte of donkerblauwe 
broekrok, (de traditionele kleding van de samurai). 
Aikido kent geen wedstrijden. 
Morihei Ueshiba (1883-1969), de grondlegger van het aikido, heeft dat bepaald om 
te voorkomen dat door invloed van lichaamskracht en conditie de pure 
aikidotechnieken verloren zouden gaan. 
Ook heeft Ueshiba dat bepaald om te voorkomen dat door competitie het principe 
van harmonie te niet gedaan zou worden. Aikido wordt wel het meest complete 
budo genoemd omdat het in technisch opzicht relaties heeft met alle budovormen 
en in de beoefening ook gebruik gemaakt wordt van zwaard- en stoktechnieken.
 